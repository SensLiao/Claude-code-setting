#!/usr/bin/env node
// qa-detect-internal-mock — PostToolUse Edit|Write|MultiEdit hook
// Detects tests that mock the module under test. Writes finding YAML for the validator.
// HIGH fix: H-01 root via walk-up
// MEDIUM fix: M-09 expanded mock-API coverage (vi.mock/doMock, jest.mock/doMock, sinon, bun:test mock.module)
// MEDIUM fix: M-01 warn-mode unchanged (PostToolUse is advisory)

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { readInput, preflight, emitAdvisory } = require('./_qa-common.js');

const input = readInput();
const pre = preflight(input);
if (pre.mode === 'silent' || pre.mode === 'off') process.exit(0);

const tool = input.tool_name || input.tool || '';
if (!['Edit', 'Write', 'MultiEdit'].includes(tool)) process.exit(0);

const filePathRaw = (input.tool_input && (input.tool_input.file_path || input.tool_input.path)) || '';
if (!filePathRaw) process.exit(0);

// Resolve against project root
let absFile = filePathRaw;
try {
  if (!path.isAbsolute(absFile) && pre.projectRoot) absFile = path.join(pre.projectRoot, absFile);
} catch {}

const matchPath = String(absFile).replace(/\\/g, '/');
const isTestFile = /\.(test|spec)\.(t|j)sx?$/i.test(matchPath) || /\/__tests__\//i.test(matchPath);
if (!isTestFile) process.exit(0);

let content;
try { content = fs.readFileSync(absFile, 'utf8'); } catch { process.exit(0); }

// Strip line/block comments to avoid false positives
const stripped = content
  .replace(/\/\*[\s\S]*?\*\//g, '')
  .replace(/(^|[^:])\/\/[^\n]*/g, (m, p1) => p1);

// Expanded mock-API detection
const MOCK_PATTERNS = [
  /(?:^|[^a-zA-Z_$])vi\.(?:mock|doMock)\s*\(\s*['"]([^'"]+)['"]/g,
  /(?:^|[^a-zA-Z_$])jest\.(?:mock|doMock|unstable_mockModule)\s*\(\s*['"]([^'"]+)['"]/g,
  /(?:^|[^a-zA-Z_$])sinon\.(?:replace|stub)\s*\(\s*['"]([^'"]+)['"]/g,
  /(?:^|[^a-zA-Z_$])td\.replace\s*\(\s*['"]([^'"]+)['"]/g,
  /(?:^|[^a-zA-Z_$])mock\.module\s*\(\s*['"]([^'"]+)['"]/g,  // bun:test
  /(?:^|[^a-zA-Z_$])proxyquire\s*\(\s*['"]([^'"]+)['"]/g,
];
const mockedModules = new Set();
for (const re of MOCK_PATTERNS) {
  re.lastIndex = 0;
  let m;
  while ((m = re.exec(stripped)) !== null) mockedModules.add(m[1]);
}
if (mockedModules.size === 0) process.exit(0);

// Capture imports + requires
const importRe = /import\s+(?:[^'"\n;]+from\s+)?['"]([^'"]+)['"]/g;
const requireRe = /\brequire\s*\(\s*['"]([^'"]+)['"]\s*\)/g;
const dynamicImportRe = /\bimport\s*\(\s*['"]([^'"]+)['"]\s*\)/g;
const imports = new Set();
for (const re of [importRe, requireRe, dynamicImportRe]) {
  re.lastIndex = 0;
  let m;
  while ((m = re.exec(stripped)) !== null) imports.add(m[1]);
}

const baseName = path.basename(absFile).replace(/\.(test|spec)\.(t|j)sx?$/i, '');
const sutCandidates = new Set([`./${baseName}`, `../${baseName}`, baseName]);

const suspicious = [];
for (const mocked of mockedModules) {
  const isInternal = mocked.startsWith('.') || mocked.startsWith('/') || mocked.startsWith('@/') || mocked.startsWith('~/');
  if (!isInternal) continue;
  const mockedBase = path.basename(mocked).replace(/\.(t|j)sx?$/i, '');
  const matchesSUT = sutCandidates.has(mocked) || mockedBase === baseName;
  const isPrimaryImport = imports.has(mocked);
  if (matchesSUT || isPrimaryImport) {
    suspicious.push({ mocked, reason: matchesSUT ? 'matches SUT basename' : 'imported as primary dependency' });
  }
}
if (suspicious.length === 0) process.exit(0);

// Write finding YAML to project root
if (!pre.projectRoot) process.exit(0);
const findingsDir = path.join(pre.projectRoot, '.qa', 'findings');
try { fs.mkdirSync(findingsDir, { recursive: true }); } catch {}
const hash = crypto.createHash('sha1').update(absFile + JSON.stringify(suspicious)).digest('hex').slice(0, 10);
const findingPath = path.join(findingsDir, `internal-mock-${hash}.yaml`);

const yaml = [
  `# Auto-generated by qa-detect-internal-mock hook`,
  `finding:`,
  `  id: internal-mock-${hash}`,
  `  detected_at: ${new Date().toISOString()}`,
  `  category: internal-module-mock`,
  `  severity: HIGH`,
  `  file: ${JSON.stringify(absFile)}`,
  `  suspicious_mocks:`,
  ...suspicious.map(s => `    - { mocked: ${JSON.stringify(s.mocked)}, reason: ${JSON.stringify(s.reason)} }`),
  `  hard_rule: §2.4 — No broad internal mocking to fake confidence`,
  `  validator_action: qa-evidence-validator should mark this layer FAIL unless justified`,
  ``,
].join('\n');
try { fs.writeFileSync(findingPath, yaml); } catch {}

const fileLabel = pre.projectRoot ? path.relative(pre.projectRoot, absFile) : absFile;
emitAdvisory('PostToolUse', [
  `[qa-detect-internal-mock] Suspicious mock detected in ${fileLabel}:`,
  ...suspicious.map(s => `  - mocked '${s.mocked}' — ${s.reason}`),
  `Hard Rule §2.4 violation candidate. Finding: ${findingPath}`,
  `qa-evidence-validator will mark this layer FAIL unless justified.`,
].join('\n'));
process.exit(0);
